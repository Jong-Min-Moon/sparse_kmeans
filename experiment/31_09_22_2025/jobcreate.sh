#!/bin/bash

# Directories
TABLE_NAME="mnist_ourmethod"
BASE_DIR="/home1/jongminm/sparse_kmeans/experiment/31_09_22_2025/${TABLE_NAME}"

# Database directory
DB_DIR="/home1/jongminm/sparse_kmeans/sparse_kmeans.db"



for rep in $(seq 1 100); do
    # Loop through repetitions

        # Define filenames for .m, .sh, and .out files
        MFILE="$BASE_DIR/${TABLE_NAME}_rep_${rep}.m"
        JOBFILE="$BASE_DIR/${TABLE_NAME}_rep_${rep}.sh"
        OUTFILE="$BASE_DIR/${TABLE_NAME}_rep_${rep}.out"

        # Create base directory if it doesn't exist
        mkdir -p "$BASE_DIR"

        # === Create .m (MATLAB) file ===
        cat > "$MFILE" <<EOF
% MATLAB script generated by bash script

% Configure local parallel cluster
pc = parallel.cluster.Local;
job_folder = fullfile('/home1/jongminm/.matlab/local_cluster_jobs/R2022a', getenv('SLURM_JOB_ID'));
if ~exist(job_folder, 'dir')
    mkdir(job_folder);
end
set(pc, 'JobStorageLocation', job_folder);

% Determine number of cores for parallel pool
ncores = str2num(getenv('SLURM_CPUS_PER_TASK')) - 1;
pool = parpool(pc, ncores);

% Define simulation parameters

rep = ${rep};
n_subsample = 1000;
n_iter = 20;
load('../29_08_11_2025/R_mnist/matlab_data.mat');
% Add sparse_kmeans path
addpath(genpath('/home1/jongminm/sparse_kmeans'));

% Database and table names
table_name = '${TABLE_NAME}';
db_dir = '${DB_DIR}';

% Data generation
gen = data_generator_subsample(selected_data', selected_integer_labels);
[data, label_true] = gen.get_data(n_subsample, rep);

% Run ISEE_kmeans_clean_simul
model = 'chain45'
ISEE_kmeans_clean_simul(data, 2, n_iter, true, 10, 5, 0.01, db_dir, table_name, rep, model, 0, label_true);

% Delete parallel pool
delete(pool);
EOF

        # === Create SLURM job script ===
        cat > "$JOBFILE" <<EOF
#!/bin/bash
#SBATCH --output="${OUTFILE}"
#SBATCH --partition=main
#SBATCH --nodes=1
#SBATCH --ntasks=1
#SBATCH --cpus-per-task=4
#SBATCH --mem=8G
#SBATCH --time=3:59:59

# Echo job start time and host
echo "Starting job for rep=${rep} on \$(hostname) at \$(date)"

# Load necessary modules
module purge
module load legacy/CentOS7
module load matlab/2022a

# Change to base directory
cd "$BASE_DIR"

# Run MATLAB script in batch mode
matlab -batch ${TABLE_NAME}_rep_${rep}_p${P}

# Echo job finish time
echo "Finished job for rep=${rep} at \$(date)"
EOF

        # === Submit job to SLURM ===
        echo "Submitting job: $JOBFILE"
        sbatch "$JOBFILE"
        # Small delay to avoid overwhelming the scheduler
        sleep 1
    done
